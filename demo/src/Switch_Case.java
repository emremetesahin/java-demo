
public class Switch_Case {

	public static void main(String[] args) {
		char grade = 'E';
		switch (grade) {
		case 'A':
			System.out.println("Notunuz A Geçtiniz");
			break;
		case 'B':
			System.out.println("Notunuz B Geçtiniz");
			break;
		case 'C':
			System.out.println("Notunuz C Geçtiniz");
			break;
		case 'D':
			System.out.println("Notunuz D Geçtiniz");
			break;
		case 'F':
			System.out.println("Notunuz F Kaldýnýz");
			break;
		default:
			System.out.println("Geçersiz Not Giriþi");
		}
		

	}

}
