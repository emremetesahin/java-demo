public class Loops {

	public static void main(String[] args) {
		//for
		for(int i=1;i<10;i++)
		{
			System.out.println(i+".For Döngüsü");
		}
		
		//while
		int i=1;
		while(i<10)
		{
			System.out.println(i+".While Döngüsü");
			i++;
		}
		//do while
		int j=1;
		do {
			System.out.println(j+".do While Döngüsü");
			j+=2;
		}while(j<10);
			
	}

}
